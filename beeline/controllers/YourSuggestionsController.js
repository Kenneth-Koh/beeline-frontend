export default [
  '$scope',
  'SuggestionService',
  'UserService',
  function (
    // Angular Tools
    $scope,
    SuggestionService,
    UserService
  ) {
    // ------------------------------------------------------------------------
    // Helper Functions
    // ------------------------------------------------------------------------

    // ------------------------------------------------------------------------
    // Data Initialization
    // ------------------------------------------------------------------------
    $scope.data = {
      suggestions: null,
    }

    // ------------------------------------------------------------------------
    // Ionic events
    // ------------------------------------------------------------------------
    // ------------------------------------------------------------------------
    // Watchers
    // ------------------------------------------------------------------------
    $scope.$watch(() => SuggestionService.getSuggestions(), suggestions => {
      $scope.data.suggestions = suggestions
    })

    $scope.$watch(
      () => UserService.getUser(),
      user => {
        SuggestionService.fetchSuggestions()
      }
    )

    // ------------------------------------------------------------------------
    // UI Hooks
    // ------------------------------------------------------------------------
  },
]
