export default [
  '$scope',
  'loadingSpinner',
  'SuggestionService',
  function (
    // Angular Tools
    $scope,
    loadingSpinner,
    SuggestionService
  ) {
    // ------------------------------------------------------------------------
    // Helper Functions
    // ------------------------------------------------------------------------

    // ------------------------------------------------------------------------
    // Data Initialization
    // ------------------------------------------------------------------------
    $scope.data = {
      suggestions: null,
    }

    // ------------------------------------------------------------------------
    // Ionic events
    // ------------------------------------------------------------------------
    // ------------------------------------------------------------------------
    // Watchers
    // ------------------------------------------------------------------------
    $scope.$watch(() => SuggestionService.getSuggestions(), suggestions => {
      $scope.data.suggestions = suggestions
    })

    // ------------------------------------------------------------------------
    // UI Hooks
    // ------------------------------------------------------------------------
  },
]
